# Numpy Built In Maths Function

from numpy import *

a = array([25, 81, 49, 64])
b = array([[4, 9, 16, 25], [36, 16, 4, 49]])

# sum()
print("Sum:")
print("A:", sum(a))
print("B:", sum(b))

# prod()
print("Prod:")
print("A:", prod(a))
print("B:", prod(b))

# sqrt()
print("Sqrt:")
print("A:", sqrt(a))
print("B:", sqrt(b))
