# sorted() function

a = "string"
b = [1, 3, 2]
c = (3, 2, 1)
d = {1, 3, 2}
e = {2: "a", 1: "b"}
f = [(2, "a"), (3, "b"), (1, "c")]
g = [[1, "a"], [3, "b"], [2, "c"]]

print("a", sorted(a))
print("b", sorted(b))
print("c", sorted(c))
print("d", sorted(d))
print("e", sorted(e))
print("f", sorted(f))
print("g", sorted(g))
