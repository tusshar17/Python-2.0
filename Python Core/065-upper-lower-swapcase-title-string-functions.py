# String Functions

# upper()
name = "helloworld"
print("orignal:", name)
print("modified:", name.upper())  # it do not change original string
print()

# lower()
name = "HELLOWORLD"
print("orignal:", name)
print("modified:", name.lower())  # it do not change original string
print()


# swapcase()
name = "HelloWorld"
print("orignal:", name)
print("modified:", name.swapcase())  # it do not change original string
print()

# title()
name = "hello world how are you"
print("orignal:", name)
print("modified:", name.title())  # it do not change original string
print()
