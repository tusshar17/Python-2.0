# isupper()

str1 = "HELLO WORLD"
str2 = "hello world"
str3 = "Hello World"

print("isupper()")
print(str1.isupper())
print(str2.isupper())
print(str3.isupper())
print()

print("islower()")
print(str1.islower())
print(str2.islower())
print(str3.islower())
print()

print("istitle()")
print(str1.istitle())
print(str2.istitle())
print(str3.istitle())
print()
