# ===== Membership Operators =====


# in

str1 = "Welocme to India"
print("to" in str1)

str2 = "Welocme top Indians"
print("to" in str2)

str1 = "Welocme to India"
print("into" in str1)


# not in

str1 = "Welcome to India"
print("into" not in str1)

str2 = "Hello World"
print("bye" not in str1)

str1 = "Welcome top coder"
