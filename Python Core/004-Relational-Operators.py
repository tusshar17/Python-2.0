# ===== Relational Operators =====


# Less than (<)
print(5 < 2)

a = 5
b = 2
value = a < b
print(value)

# Greater than(>)
print(5 > 2)

a = 5
b = 2
value = a > b
print(value)

# Less than or Equal to (<=)
print(5 <= 2)

a = 5
b = 2
value = a <= b
print(value)

# Greater than or Equal to
print(5 >= 2)

a = 5
b = 2
value = a >= b
print(value)

# Equal to
print(5 == 2)

a = 5
b = 2
value = a == b
print(value)

# Not Equal to
print(5 != 2)

a = 5
b = 2
value = a != b
print(value)
