# ===== Arithmetic Operators =====


# Addition (+)
print(4+2)

a = 4
b = 2
total = a + b
print(total)


# Subtraction (-)
print(4-2)

a = 4
b = 2
sub = a - b
print(sub)


# Multiplication (*)
print(4*2)

a = 4
b = 2
multi = a * b
print(multi)


# Division (/)
print(4/2)

a = 4
b = 2
div = a / b
print(div)
print(type(div))


# Modulus (%)
print(5 % 2)

a = 5
b = 2
mod = a % b
print(mod)


# Exponent (**)
print(5**2)

a = 5
b = 2
exp = a ** b
print(exp)


# Floor Division (//)
print(5//2)

a = 5
b = 2
sub = a // b
print(sub)

a = -5
b = 2
sub = a // b
print(sub)
